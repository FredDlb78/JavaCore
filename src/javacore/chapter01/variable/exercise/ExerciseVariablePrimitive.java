package javacore.chapter01.variable.exercise;

public class ExerciseVariablePrimitive {

    public static void main(String[] args) {

        //Numéro ISBN d'un livre : un numéro d'identification à 13 chiffres.
        long ISBN;
        //Nombre de pages d'un livre : ne dépasse généralement pas 5000.
        short pages;
        //Note moyenne d'un livre : basée sur les avis des utilisateurs, variant de 0 à 5 et pouvant avoir une valeur à virgule.
        float averageRate;
        //Statut d'emprunt d'un livre : indique si le livre est disponible ou emprunté.
        boolean isAvailable;
        //Nombre total d'utilisateurs inscrits à la bibliothèque : la bibliothèque est petite et ne devrait (en théorie) pas avoir plus de 2000 utilisateurs.
        short registeredUsers;
        //Solde d'amende d'un utilisateur : montant dû pour les retards, pouvant inclure des centimes.
        float fineBalance;
        //Code unique de l'utilisateur : un identifiant numérique à 6 chiffres.
        int userId;

    }
}
